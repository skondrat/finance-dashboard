export function SettingsMenu() {
  const router = useRouter();
  const [open, setOpen] = useState(false);
  const ref = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (!open) return;
    function handleClickOutside(e: MouseEvent) {
      if (ref.current && !ref.current.contains(e.target as Node)) {
        setOpen(false);
      }
    }
    document.addEventListener("mousedown", handleClickOutside);
    return () => document.removeEventListener("mousedown", handleClickOutside);
  }, [open]);

  return (
    <div ref={ref} className="relative">
      <button
        onClick={() => setOpen((prev) => !prev)}
        className="bg-surface-container-high rounded-full w-8 h-8 flex items-center justify-center text-on-surface-variant hover:text-on-surface transition-colors"
        aria-label="Settings"
      >
        <GearIcon />
      </button>

      {open && (
        <div className="absolute right-0 top-full mt-1 z-50 min-w-[140px] rounded-lg bg-surface-container-lowest border border-on-surface-variant/10 shadow-ambient py-1">
          <button
            onClick={() => {
              setOpen(false);
              router.push("/budget/categories");
            }}
            className="w-full text-left px-3 py-2 font-mono text-xs uppercase tracking-[0.1em] text-on-surface hover:bg-surface-container-low transition-colors"
          >
            Categories
          </button>
        </div>
      )}
    </div>
  );
}
